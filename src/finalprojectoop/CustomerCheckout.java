    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (nameTF.getText().matches("(\\w{2,10})((\\s)(\\w{2,10})){0,2}") && paymentTF.getText().matches("\\d{1,}(.){1}\\d{2}") && Double.valueOf(paymentTF.getText()) > 0) {
            if (jComboBox1.getSelectedIndex() == 0) {
                FinalProjectOOP.customer = new NormCustomer(nameTF.getText(), Double.valueOf(paymentTF.getText()), FinalProjectOOP.cart);
                jButton2.setVisible(false);
            }
            if (jComboBox1.getSelectedIndex() == 1) {
                FinalProjectOOP.customer = new SilverCustomer(nameTF.getText(), Double.valueOf(paymentTF.getText()), FinalProjectOOP.cart);
                jButton2.setVisible(false);
            }
            if (jComboBox1.getSelectedIndex() == 2) {
                FinalProjectOOP.customer = new GoldCustomer(nameTF.getText(), Double.valueOf(paymentTF.getText()), FinalProjectOOP.cart);
                jButton2.setVisible(false);
            }
        } else {
            //Invalid format of name or payment
            Error error = new Error();
            error.errorLB.setText("Invalid format of name or payment");
            error.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

