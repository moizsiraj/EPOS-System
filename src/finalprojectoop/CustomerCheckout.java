    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String print = "";
        CartReport cartRep = new CartReport();
        print = print + "Name\tQuantity\tPrice\tBarCode\n";
        for (int i = 0; i < FinalProjectOOP.cart.size(); i++) {
            Product x = FinalProjectOOP.cart.traverse(i);
            print = print + x.getName() + "\t" + x.getQuantity() + "\t" + x.getPrice() + "\t" + x.getBarCode() + "\n";
        }
        cartRep.jTextArea1.setText(print);
        cartRep.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void checkoutBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutBTNActionPerformed
        DecimalFormat df = new DecimalFormat("0.00");
        if (FinalProjectOOP.cart.getNoOfItems() > 0) {
            Bill bill = new Bill();
            bill.backBtn.setVisible(false);
            Order order = new Order(FinalProjectOOP.cart);
            order.total();
            bill.totalLB.setText(df.format(order.getTotal()));
            discount Discount = new discount(FinalProjectOOP.customer);
            if (FinalProjectOOP.customer.pay(order, FinalProjectOOP.customer, FinalProjectOOP.cart, Discount)) {
                bill.cusLB.setText(FinalProjectOOP.customer.getName());
                bill.apLB.setText(df.format(FinalProjectOOP.customer.getPayment()));
                bill.balLB.setText(df.format(FinalProjectOOP.customer.getBalance()));
                bill.disLB.setText(df.format(Discount.amountDiscount(order)));
                bill.noofitemsLB.setText(Integer.toString(FinalProjectOOP.cart.getNoOfItems()));
                bill.paidLB.setText(df.format(FinalProjectOOP.customer.getTotal()));
                String print = "";
                print = print + "Name\tQuantity\tPrice\tBarCode\n";
                for (int i = 0; i < FinalProjectOOP.cart.size(); i++) {
                    Product x = FinalProjectOOP.cart.traverse(i);
                    print = print + x.getName() + "\t" + x.getQuantity() + "\t" + x.getPrice() + "\t" + x.getBarCode() + "\n";
                }
                bill.billTA.setText(print);
                this.dispose();
                bill.setVisible(true);
            } else {
                //insuffienct amount
                this.jButton5.setVisible(true);
                bill.cusLB.setText(FinalProjectOOP.customer.getName());
                bill.apLB.setText(df.format(FinalProjectOOP.customer.getPayment()));
                bill.setVisible(true);
                Error error = new Error();
                error.errorLB.setText("Insuffienct amount");
                error.setVisible(true);
                bill.jButton2.setVisible(false);
                bill.backBtn.setVisible(true);
            }
        } else {
            //No item in cart
            Error error = new Error();
            error.errorLB.setText("Cart is empty");
            error.setVisible(true);
        }
    }//GEN-LAST:event_checkoutBTNActionPerformed

